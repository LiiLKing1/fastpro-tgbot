def get_msg(user_lang: str, key: str, **kwargs) -> str:
    lang_dict = LANGUAGES.get(user_lang, LANGUAGES['uz'])
    text = lang_dict.get(key, LANGUAGES['uz'].get(key, key))
    if kwargs:
        return text.format(**kwargs)
    return text
